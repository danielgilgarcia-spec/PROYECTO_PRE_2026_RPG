def _place_exit(base):
    """Devuelve una copia del mapa con el tile 5 en una celda de pasto aleatoria."""
    mapa = copy.deepcopy(base)
    walkable = [
        (y, x)
        for y, row in enumerate(mapa)
        for x, tile in enumerate(row)
        if tile == 0
    ]
    if walkable:
        ey, ex = random.choice(walkable)
        mapa[ey][ex] = 5
    return mapa


